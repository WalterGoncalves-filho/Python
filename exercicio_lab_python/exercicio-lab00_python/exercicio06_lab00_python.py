# DIVISÃO
#Quatro amigos foram a um restaurante. A conta deu R$ 180, a ser repartida igualmente para cada um.
#Escreva um programa que imprima o valor que cada um tem que desembolsar.

# DICAS: 

# O sinal de divisão é a barra (/).
# Use a função print() para imprimir o resultado da divisão no console.
# Expressões numéricas NÃO precisam do sinal de aspas ("").
# O exemplo de saída abaixo fornece um modelo de formatação da saída, e não o valor da resposta esperada.

#PARTE LÓGICA:

total_da_conta = 180.00
total_de_amigos = int(4.0)
valor_de_cada_um_a_ser_pago = float(total_da_conta / total_de_amigos) 

#SAÍDA:


print(valor_de_cada_um_a_ser_pago)


