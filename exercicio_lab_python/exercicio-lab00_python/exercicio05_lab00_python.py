#MULTIPLICAÇÃO

#Vinte amigos foram a um rodízio de pizza. Cada um gastou R$ 35.
#Escreva um programa que imprima o valor total da conta.

# DICAS: 

# O sinal de multiplicação é o asterisco (*).
# Use a função print() para imprimir o resultado da multiplicação no console.
# Expressões numéricas NÃO precisam do sinal de aspas ("").
# O exemplo de saída abaixo fornece um modelo de formatação da saída, e não o valor da resposta esperada.

# Parte Lógica: 

amigos = 20
gasto_por_amigo = 35.00
gasto_total = (amigos * gasto_por_amigo)

#Saída: 

print(gasto_total)